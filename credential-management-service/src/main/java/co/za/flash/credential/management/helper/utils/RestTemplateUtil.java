    public static boolean hasInterceptor(List<ClientHttpRequestInterceptor> interceptors, Class cls) {
        for (int i = 0; i < interceptors.size(); i++) {
            if (interceptors.get(i).getClass() == cls)
                return true;
        }
        return false;
    }

    private static boolean hasAuthInterceptors(List<ClientHttpRequestInterceptor> interceptors) {
        return hasInterceptor(interceptors, BasicAuthenticationInterceptor.class) ||
                hasInterceptor(interceptors, TokenInterceptor.class);
    }

    private static void removeAuthHeaders(List<ClientHttpRequestInterceptor> interceptors) {
        ListIterator<ClientHttpRequestInterceptor> iter = interceptors.listIterator();
        while(iter.hasNext()){
            if(iter.next().getClass() == BasicAuthenticationInterceptor.class ||
                iter.next().getClass() == TokenInterceptor.class){
                iter.remove();
            }
        }
    }

